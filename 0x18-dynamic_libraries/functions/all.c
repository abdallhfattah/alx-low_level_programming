/**
 * add : adding two values
 * @num1 : parameter
 * @num2 : parameter
 * Return: the sum of the two number
 */

int add(int num1, int num2)
{
	return (num1 + num2);
}

/**
 * sub : subtract 2 numbers
 * @num1 : parameter
 * @num2 : parameter
 * Return: the subtraction of the two number
 */

int sub(int num1, int num2)
{
	return (num1 - num2);
}

/**
 * add : multiply two values
 * @num1 : parameter
 * @num2 : parameter
 * Return: the multiplication of the two number
 */

int mul(int num1, int num2)
{
	return (num1 * num2);
}

/**
 * div : diving two values
 * @num1 : parameter
 * @num2 : parameter
 * Return: the division of the two number
 */

int div(int num1, int num2)
{
	return (num1 / num2);
}

/**
 * mod : finds mod of two numbers
 * @num1 : parameter
 * @num2 : parameter
 * Return: the mod of the two number
 */

int mod(int num1, int num2)
{
	return (num1 % num2);
}
